@Composable
fun AdoptedCatsList(cats: List<Cat>) {
    val context = LocalContext.current

    LazyColumn {
        items(cats) { cat ->
            CatItemView(cat = cat) {
                startCatDetail(context, cat)
            }
        }
    }
}

private fun startCatDetail(context: Context, cat: Cat) {
    context.startActivity(DetailActivity.newIntent(context, cat))
}